def zadoff_chu(u: int, Nzc: int) -> np.ndarray:
    """Return the NR Zadoff-Chu base sequence x_u of length ``Nzc``.

    Implements the form used in 38.211 §5.2.2.1:

        x_u(n) = exp(-j · π · u · n · (n+1) / Nzc),  n = 0..Nzc-1

    The sequence has unit magnitude and ideal periodic auto-correlation when
    ``Nzc`` and ``u`` are coprime (and in particular for prime ``Nzc``).

    Parameters
    ----------
    u:
        Root index. Must be coprime to ``Nzc``.
    Nzc:
        Sequence length, typically the largest prime < Msc_RS.
    """
    if Nzc <= 0:
        raise ValueError("Nzc must be positive")
    if not (0 < u < Nzc):
        raise ValueError("u must satisfy 0 < u < Nzc")
    n = np.arange(Nzc, dtype=np.int64)
    phase = -np.pi * float(u) * n.astype(np.float64) * (n + 1).astype(np.float64) / float(Nzc)
    return np.exp(1j * phase).astype(np.complex128)


def _next_prime_below(n: int) -> int:
    """Largest prime strictly less than ``n``. Used to pick Nzc from Msc."""
    if n <= 2:
        raise ValueError("No prime < n for n <= 2")
    candidate = n - 1
    while candidate >= 2:
        if _is_prime(candidate):
            return candidate
        candidate -= 1
    raise ValueError("No prime found")


def _is_prime(n: int) -> bool:
    if n < 2:
        return False
    if n < 4:
        return True
    if n % 2 == 0:
        return False
    r = math.isqrt(n)
    return all(n % i != 0 for i in range(3, r + 1, 2))


def r_uv_long(u: int, v: int, Nzc: int) -> np.ndarray:
    """Long base sequence for Msc_RS ≥ 36 per 38.211 §5.2.2.1.

    Builds ``r̄_{u,v}(n) = x_q(n mod Nzc)`` with ``x_q`` the Zadoff-Chu sequence
    of root ``q`` derived from ``(u, v, Nzc)``:

        q = floor(q̄ + 1/2) + v · (-1)^floor(2 q̄)
        q̄ = Nzc · (u + 1) / 31

    Parameters
    ----------
    u, v:
        Sequence group / sequence number (0 ≤ u < 30, v ∈ {0, 1}).
    Nzc:
        Prime ZC length.
    """
    if not (0 <= u < 30):
        raise ValueError("u must be in [0, 30)")
    if v not in (0, 1):
        raise ValueError("v must be 0 or 1")
    q_bar = Nzc * (u + 1) / 31.0
    q = int(np.floor(q_bar + 0.5)) + v * int((-1) ** int(np.floor(2.0 * q_bar)))
    # Reduce q modulo Nzc so the ZC root is valid.
    q_mod = q % Nzc
    if q_mod == 0:
        q_mod = 1  # Guard: should not happen for valid (u, Nzc) pairs.
    return zadoff_chu(q_mod, Nzc)


def r_uv_short(u: int, v: int, Nzc: int) -> np.ndarray:
    """Short base sequence for Msc_RS ∈ {6, 12, 18, 24} per 38.211 §5.2.2.2.

    Uses the computer-generated φ-tables.  Returns

        r̄_{u,v}(n) = exp(j · φ(n) · π / 4),   n = 0..Nzc-1

    ``v`` is ignored for these short lengths (only u indexes the table).
    """
    del v  # Not used in short tables; kept for API symmetry.
    if not (0 <= u < 30):
        raise ValueError("u must be in [0, 30)")
    table_map = {
        6: PHI_TABLE_M6,
        12: PHI_TABLE_M12,
        18: PHI_TABLE_M18,
        24: PHI_TABLE_M24,
    }
    if Nzc not in table_map:
        raise ValueError(f"Unsupported short length Nzc={Nzc}; expected one of {sorted(table_map)}")
    phi = table_map[Nzc][u].astype(np.float64)
    return np.exp(1j * np.pi * phi / 4.0).astype(np.complex128)
